/**
 * @brief Вычисляет Morton (Z-order) смещение пикселя внутри тайла 8×8.
 *
 * Используется при записи пикселей в Morton-упорядоченный буфер текстуры.
 *
 * Чередование битов:
 *   бит 0 результата = бит 0 localX
 *   бит 1 результата = бит 0 localY
 *   бит 2 результата = бит 1 localX
 *   бит 3 результата = бит 1 localY
 *   бит 4 результата = бит 2 localX
 *   бит 5 результата = бит 2 localY
 *
 * @param localX X внутри тайла [0..7]
 * @param localY Y внутри тайла [0..7]
 * @return Смещение пикселя внутри тайла [0..63]
 */
static inline u32 calcMortonOffset(u32 localX, u32 localY) {
    return  (localX & 1)        |
           ((localY & 1) << 1)  |
           ((localX & 2) << 1)  |
           ((localY & 2) << 2)  |
           ((localX & 4) << 2)  |
           ((localY & 4) << 3);
}

/**
 * @brief Поворачивает 2D-точку вокруг начала координат.
 *
 * Используется при рисовании повёрнутых спрайтов. Формула:
 *   ox =  cs * x + sn * y
 *   oy = -sn * x + cs * y
 *
 * Знак sn по Y перевёрнут, потому что ось Y экрана направлена вниз
 * (0 — верх, растёт вниз), в отличие от математической системы координат.
 *
 * @param x, y   Исходная точка
 * @param cs     cos(угол_в_радианах)
 * @param sn     sin(угол_в_радианах)
 * @param ox, oy [out] Повёрнутая точка
 */
static inline void rotatePoint(float x, float y,
                                float cs, float sn,
                                float *ox, float *oy) {
    *ox =  cs * x + sn * y;
    *oy = -sn * x + cs * y;
}

