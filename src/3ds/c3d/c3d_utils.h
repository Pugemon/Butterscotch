/**
 * @file c3d_utils.h
 * @brief Низкоуровневые GPU-утилиты для PICA200 / Citro3D.
 *
 * Здесь сосредоточены функции, которые напрямую работают с особенностями
 * GPU Nintendo 3DS: форматами пикселей, адресацией памяти, матрицами поворота.
 *
 * ## Что такое Morton Z-order (свиззл)?
 *
 * Обычные изображения хранятся линейно: пиксель (x, y) находится по адресу
 * `y * width + x`. GPU при сэмплировании текстуры обращается к соседним пикселям
 * (2×2 или 4×4 блоки) — при линейном хранении они разбросаны по памяти, что
 * приводит к промахам кэша.
 *
 * Morton/Z-order решает это: текстура делится на тайлы 8×8, внутри каждого тайла
 * пиксели упорядочены так, что биты координат X и Y чередуются:
 *
 *   адрес = ... | Y2 | X2 | Y1 | X1 | Y0 | X0 |
 *
 * Это обеспечивает хорошую локальность при обращении к квадратным блокам пикселей.
 * PICA200 аппаратно ожидает именно этот формат для всех текстур.
 */

/**
 * @brief Следующая степень двойки, не меньше TEX_MIN_SIZE.
 *
 * PICA200 требует POT-размеры (Power Of Two) для текстур: 8, 16, 32, 64, ...
 * Если исходный размер уже POT — он возвращается без изменений.
 *
 * Примеры: nextPOT(1) → 8, nextPOT(100) → 128, nextPOT(256) → 256.
 *
 * @param n Исходный размер в пикселях
 * @return Ближайшая POT >= n, но не меньше TEX_MIN_SIZE
 */
u32 nextPOT(u32 n);

/**
 * @brief Конвертирует цвет движка (BGR + отдельная альфа) в формат ABGR для PICA200.
 *
 * В движке цвет хранится как 24-битный BGR (0x00BBGGRR), а альфа — отдельный float.
 * Вершина шейдера PICA200 ожидает цвет в формате ABGR:
 *   байт 0 (младший) = R
 *   байт 1           = G
 *   байт 2           = B
 *   байт 3 (старший) = A
 *
 * @param bgr  Цвет в формате 0x00BBGGRR
 * @param alpha Прозрачность [0.0 = прозрачно, 1.0 = непрозрачно]
 * @return u32 в формате ABGR
 */
static inline u32 colorToABGR(uint32_t bgr, float alpha) {
    // Если прозрачности нет (95% случаев), просто ставим байт альфы в 0xFF
    if (alpha >= 1.0f) return bgr | 0xFF000000u;

    // Иначе заменяем старший байт на нашу вычисленную альфу
    return bgr | (((u32)(alpha * 255.0f)) << 24);
}


