// ─── Экран ────────────────────────────────────────────────────────────────────

/**
 * Физический top-экран 3DS аппаратно повёрнут на 90°.
 * GPU видит буфер как SCREEN_MEM_WIDTH × SCREEN_MEM_HEIGHT,
 * хотя пользователь видит его как 400×240.
 *
 *   Физически (глазами):  400 px широкий, 240 px высокий
 *   В памяти GPU:         240 px "широкий", 400 px "высокий"
 *
 * Все вызовы C3D_RenderTargetCreate, C3D_SetViewport и т.п. принимают
 * именно "памятные" размеры (MEM), а не физические (PHYS).
 */
#define SCREEN_MEM_WIDTH   240  ///< Ширина буфера в памяти (= физическая высота)
#define SCREEN_MEM_HEIGHT  400  ///< Высота буфера в памяти (= физическая ширина)

// ─── Текстуры ─────────────────────────────────────────────────────────────────

/**
 * PICA200 требует POT-размеры (Power Of Two) для всех текстур,
 * и минимальный допустимый размер по каждой оси — 8 пикселей.
 */
#define TEX_MIN_SIZE 8  ///< Минимальный размер текстуры на PICA200 (px)

/**
 * Белая текстура (WHITE_TEX_SIZE × WHITE_TEX_SIZE) используется как "заглушка"
 * при рисовании примитивов (прямоугольники, линии) без пользовательской текстуры.
 *
 * Шейдер вычисляет: finalColor = vertexColor * textureColor (GPU_MODULATE).
 * Умножение на белый (1, 1, 1, 1) не меняет цвет вершины — именно это нам нужно.
 */
#define WHITE_TEX_SIZE   8                             ///< Размер белой текстуры (px)
#define WHITE_TEX_PIXELS (WHITE_TEX_SIZE * WHITE_TEX_SIZE)  ///< Число пикселей (64)
#define WHITE_COLOR_ABGR 0xFFFFFFFFu  ///< Белый в формате ABGR (A=FF, B=FF, G=FF, R=FF)

/**
 * Бюджет VRAM для игровых текстур (в байтах).
 *
 * LRU вытесняет текстуры в цикле, пока суммарный объём загруженных текстур
 * не опустится ниже этого порога. Лимит по байтам точнее, чем лимит по штукам:
 * маленький глиф 32×32 ETC1A4 = 512 байт, большой атлас 1024×1024 = 512 KiB —
 * ограничение по числу текстур не учитывает эту разницу.
 *
 * Ориентир по памяти 3DS:
 *   Linear Heap (linearAlloc): ≈ 32–64 MiB (зависит от конфигурации)
 *   Рекомендуемый бюджет:       4 MiB — оставляет запас для VBO, стека и прочего
 *
 *   Примеры текстур ETC1A4 (8 бит/пиксель = 1 байт на пиксель):
 *     1024×1024 = 1 MiB
 *      512×512  = 256 KiB
 *      256×256  = 64 KiB
 *   4 MiB → ≈ 4 больших атласа или ≈ 16 средних одновременно.
 */
#define VRAM_BUDGET_BYTES (4u * 1024u * 1024u)  ///< 4 MiB

/**
 * Количество максимально загруженных текстур в VRAM
 * Fallback-поведение для png-бекенда текстур при рисовании напрямую из data.win.
 * Занимаемый объём не может быть предварительно просчитан, поэтому используется примитивный кеш по количеству.
 */
#define MAX_CACHED_TEXTURES 8

// ─── VBO / Батч ───────────────────────────────────────────────────────────────

/**
 * Максимальное число вершин в вершинном буфере (VBO) за один кадр.
 *
 * Один спрайт = 2 треугольника = 6 вершин.
 * MAX_VERTICES / 6 ≈ 10922 спрайтов за кадр без промежуточного сброса.
 *
 * VBO выделяется в linearAlloc — регионе непрерывной физической памяти,
 * доступной DMA-контроллеру GPU напрямую без копирования.
 * Обычный malloc() для VBO не подходит — GPU его не увидит.
 */
#define MAX_VERTICES 65536

// ─── Вершинные атрибуты ───────────────────────────────────────────────────────

/**
 * Количество атрибутов в C3D_Vertex:
 *   attrib 0 — position  (float3: x, y, z)
 *   attrib 1 — texcoord  (float2: u, v)
 *   attrib 2 — color     (ubyte4: R, G, B, A упакованы в u32)
 */
#define VERTEX_ATTR_COUNT 3

/**
 * Permutation для BufInfo_Add: задаёт маппинг входных слотов шейдера
 * на атрибуты в буфере.
 *
 * 0x210 = 0b 0010 0001 0000
 * Читается с младших 4 бит по 4 бита на слот:
 *   slot 0 ← attrib 0   (биты [3:0]  = 0x0)
 *   slot 1 ← attrib 1   (биты [7:4]  = 0x1)
 *   slot 2 ← attrib 2   (биты [11:8] = 0x2)
 * Итого: v0=position, v1=texcoord, v2=color — ровно в порядке определения в шейдере.
 */
#define VERTEX_ATTR_PERMUTATION 0x210

// ─── 2D-рендеринг ─────────────────────────────────────────────────────────────

/**
 * Z-координата всех 2D-спрайтов.
 *
 * Тест глубины отключён (C3D_DepthTest(false, ...)), поэтому значение Z
 * не влияет на порядок отрисовки — он определяется порядком вызовов draw.
 * Значение 0.5 — середина стандартного диапазона глубины [0, 1].
 */
#define SPRITE_Z_DEPTH 0.5f

/**
 * Минимальная длина линии для рисования.
 *
 * Если |line| < LINE_MIN_LENGTH, вызов игнорируется.
 * Это предотвращает деление на ноль при вычислении нормали к вырожденной линии.
 */
#define LINE_MIN_LENGTH 0.001f

// ─── Цвет фона ────────────────────────────────────────────────────────────────

/**
 * Цвет очистки буфера кадра в формате RGBA8 (0xRRGGBBAA).
 * Передаётся в C3D_RenderTargetClear с флагом C3D_CLEAR_ALL.
 *
 * 0x000000FF = чёрный, полностью непрозрачный.
 */
#define CLEAR_COLOR 0x000000FFu

// ─── Дефолтный стейт рендерера ────────────────────────────────────────────────

#define DEFAULT_DRAW_COLOR 0xFFFFFF  ///< Цвет рисования по умолчанию: белый (BGR)
#define DEFAULT_DRAW_ALPHA 1.0f      ///< Альфа по умолчанию: полностью непрозрачно
#define DEFAULT_DRAW_FONT  (-1)      ///< Шрифт по умолчанию: не задан
